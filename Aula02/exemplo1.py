valor1 = 8
valor2 = 11
#operadores aritméticos (continuação)
print('Exponenciação:', valor1**3)
print('Divisão padrão:',valor1/valor2)
print('Divisão sem decimais:', valor1//valor2)
print('Módulo(resto) da divisão:', valor2%3)
#operadores de comparação (relacionais)
print('Valor 1 é maior do que valor 2:',valor1>valor2)
print('Valor 1 é maior ou igual a valor 2:',valor1>=valor2)
print('Valor 1 é menor do que valor 2:',valor1<valor2)
print('Valor 1 é menor ou igual a valor 2:',valor1<=valor2)
print('Valor 1 é igual a valor 2:',valor1==valor2)
print('Valor é diferente de valor2:',valor1!=valor2)